# This is a list of strings
product_categories = ["Electronics", "Home Goods", "Office Supplies", "Books"]

# You can print the whole list
print("Our product categories:", product_categories)

# You can access individual items using their position (index)
# IMPORTANT: Python counts from 0! (We'll talk about why in a moment)
print("First category:", product_categories[0])  # Output: Electronics
print("Second category:", product_categories[1])  # Output: Home Goods